#!/bin/bash
# =============================================================================
# Semantic Router Test Runner
# Прогоняет тестовые промпты через хук и показывает только выбранные файлы
#
# Запуск: bash scripts/ai/run-tests.sh
# =============================================================================

set -u

PROJECT_DIR="$(cd "$(dirname "$0")/../.." && pwd)"
HOOK="$PROJECT_DIR/.claude/hooks/semantic-router.sh"

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

if [ ! -f "$HOOK" ]; then
  echo -e "${RED}ERROR: semantic-router.sh not found${NC}"
  exit 1
fi

if [ ! -f "$PROJECT_DIR/.semantic-index.json" ]; then
  echo -e "${RED}ERROR: .semantic-index.json not found. Run: bash scripts/ai/build-index.sh${NC}"
  exit 1
fi

# Тестовые промпты: "НАЗВАНИЕ|ПРОМПТ"
TESTS=(
  # СЛОЖНЫЕ РЕАЛЬНЫЕ ПРОМПТЫ ПОЛЬЗОВАТЕЛЯ
  "Timeline перекрывает табы|у на сейчас есть timeline который отображает слева в терминале. есть кнопка которая открывает слева от таймлайн историю. Необходимо поменять разметку где у нас она открывается. То есть сейчас у меня timeline отображается до конца с табами. нужно чтобы он отображался только до конца окна терминала. То есть он не должен захватывать границу, типа верхнюю границу табов. Вот то же самое получается вкладка истории она должна также заканчиваться на верхней границе терминала сейчас она вообще как-то непонятно работает плюс более того у нас проблема что она как-то ебашит разметку то есть она создает по высоте лишнее пространство ???"
  "Клик сбрасывает скролл|плюс еще нужно добавить один fix когда я кликаю на timeline сейчас на каждое сообщение у меня автоматически открывается полная история и переход на сообщение в истории хотя если у меня история не открыта то у меня не должно быть перехода по ней. то есть если у меня полная история не открыто то у меня клик должен срабатывать внутри терминал если у меня история открыта то у меня у меня при клике на каждое сообщение клик не должен срабатывать на окне самого терминала только на окне истории ну ты понял ???"
  "Меню улетает за край|Ничего, блять, у нас по скриншоту какая-то хуйня произошла. Во-первых, у нас почему-то обрезался блок, связанный с копированием сессии. Единственное, что у нас отображается нормально, это при наведении на копировать текущую сессию слева в правильной границе отображается блок параметры. И то он отображается не посередине, а снизу, так что это ошибка. При наведении на обновление документации блок отображается не на границе левой вот этого блока. Он отображается где-то, блять, на пиксели 10 вправо. <div class=\"text-[11px] uppercase text-[#888] mb-2\">AI Session</div> ???"
  "Моргание превью|Также у нас сейчас в таймлайне есть одна такая прикольная тема, что при обычном таймлайне, когда мы навигируемся, листаем, короче, скролл, у нас считывается текст, который внутри терминала. И в зависимости от этого таймлайн справа помещается. Короче, там анимация срабатывает как полоска. Посмотри, как это устроено. И можем ли мы то же самое сделать когда у нас открыт таймлайн при полной истории? То есть чтобы у нас было то же самое. Но это кстати даже наверное легче будет, потому что при полной истории у нас по-моему по-другому работает через айдишники или типа того. ???"

  # Оставленный старый тест (бывший 11 про ui)
  "Range copy empty|Копирование диапазона сообщений из Timeline иногда возвращает пустые данные ???"
)

TOTAL=${#TESTS[@]}
RUN_ONLY="${1:-}"

echo -e "${BLUE}=== Semantic Router Test Suite (Inspection Mode) ===${NC}"
echo -e "${BLUE}Tests: $TOTAL | Index: $(jq 'length' "$PROJECT_DIR/.semantic-index.json") files${NC}"
if [ -n "$RUN_ONLY" ]; then
  echo -e "${YELLOW}Running only test #$RUN_ONLY${NC}"
fi
echo ""

for i in "${!TESTS[@]}"; do
  IFS='|' read -r NAME PROMPT <<< "${TESTS[$i]}"
  NUM=$((i + 1))

  # Пропускаем если указан конкретный тест
  if [ -n "$RUN_ONLY" ] && [ "$NUM" != "$RUN_ONLY" ]; then
    continue
  fi

  # Запускаем хук
  START_TIME=$(date +%s)
  RESULT=$(jq -n --arg p "$PROMPT" '{"prompt":$p}' | CLAUDE_PROJECT_DIR="$PROJECT_DIR" bash "$HOOK" 2>/dev/null | head -1)
  END_TIME=$(date +%s)
  ELAPSED=$((END_TIME - START_TIME))

  # Извлекаем выбранные файлы
  SELECTED=$(echo "$RESULT" | sed 's/.*Haiku selected: //' | tr -d ' ')

  echo -e "${BLUE}[$NUM/$TOTAL]${NC} $NAME [${ELAPSED}s]"
  echo -e "  Got: ${CYAN}$SELECTED${NC}"
  echo ""
done

echo -e "${BLUE}========================================${NC}"
echo -e "${BLUE}Review the results above and decide which files are correct${NC}"
echo -e "${BLUE}========================================${NC}"
